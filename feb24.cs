// We learned how to use variables and create a sentence with them. We did this by storing the variables and then using them in a sentence by using "Console.Write".
string name = "Bob";
int messages = 3;
decimal temperature = 34.4m;
Console.Write("Hello, ");
Console.Write(name);
Console.Write("! You have ");
Console.Write(messages);
Console.Write(" in your inbox. The temperature is ");
Console.Write(temperature);
Console.Write(" celsius.");
